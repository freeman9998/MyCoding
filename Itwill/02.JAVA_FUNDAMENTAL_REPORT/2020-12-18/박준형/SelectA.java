package com.itwill03.포함;

public class SelectA {
	private int x;
	private int y;
	
	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
	public String toString() {
		return "SelectA [x=" + x + ", y=" + y + "]";
	}	
}
