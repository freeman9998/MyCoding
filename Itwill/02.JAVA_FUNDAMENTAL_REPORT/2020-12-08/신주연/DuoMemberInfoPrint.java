package com.itwill00.variable;

public class DuoMemberInfoPrint {

	public static void main(String[] args) {
		
		System.out.println("*************************");
		
		int num;
		num = 181818;
		System.out.println("회원번호 : " + num);
		
		char name1,name2,name3;
		name1 = '홍';
		name2 = '길';
		name3 = '동';
		System.out.println("이    름 : " + name1+name2+name3);
		
		int no1,no2;
		no1 = 201212;
		no2 = 3495039;
		System.out.println("주민번호 : "+ no1 +"-" + no2);
		
		String sex;
		sex = "male";
		System.out.println("성    별 : " + sex);
		
		byte marry;
		marry = 0;
		System.out.println("결혼횟수 : "+ marry);
		
		boolean smoke;
		smoke = true;
		System.out.println("흡연여부 : "+smoke);
		
		
		
		System.out.println("*************************");
		
	
	}

}
