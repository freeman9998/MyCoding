package report1215;

/*
 * 4개의 멤버메쏘드를 가짐
 */
public class Calculator {
	/*
	 * add( 두개의 정수를 인수로 받아서 더한결과를 반환)
	 * substract
	 * div
	 * multiple
	 */
	
	public int add(int a, int b) {
		return a+b;
	}

	public int substract(int a, int b) {
		return a-b;
	}

	public double div(int a, int b) {
		return a/b;
	}

	public int multiple(int a, int b) {
		return a*b;
	}
	
}