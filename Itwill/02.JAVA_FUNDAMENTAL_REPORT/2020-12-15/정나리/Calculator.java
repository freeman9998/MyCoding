package com.itwill01.method;
/*
 * 4개의 멤버메쏘드를 가짐
 */
public class Calculator {
	
	
	 //- add( 두개의 정수를 인수로 받아서 더한결과를 반환)
	int add(int a, int b) {
		return a+b;
	}
	
	 //- sub 두개의 정수를 인수로 받아서 뺀결과를 반환)
	int sub(int a, int b) {
		return a-b;
	}
	
	 //- div( 두개의 정수를 인수로 받아서 나눈결과를 반환)
	int div(int a, int b) {
		return a/b;
	}
	
	 //- mul( 두개의 정수를 인수로 받아서 곱한결과를 반환)
	int mul(int a, int b) {
		return a*b;
	}
	
	 
}
