7. 사원테이블에서 사원명, 해당 사원의 관리자명, 해당 사원의 관리자의 관리자명을 검색하시오.
--ORACLE JOIN
SELECT E.ENAME 사원명, M1.ENAME 관리자명, M2.ENAME 관리자의관리자명
FROM EMP E,EMP M1,EMP M2
WHERE E.MGR=M1.EMPNO AND M1.MGR=M2.EMPNO;
--ANSI JOIN
SELECT E.ENAME 사원명, M1.ENAME 관리자명, M2.ENAME 관리자의관리자명
FROM EMP E JOIN EMP M1
ON E.MGR=M1.EMPNO 
JOIN EMP M2
ON M1.MGR=M2.EMPNO;
8. 7번 결과에서 상위 관리자가 없는 모든 사원의 이름도 사원명에 출력되도록 수정하시오.
--ORACLE JOIN
SELECT E.ENAME 사원명, M1.ENAME 관리자명, M2.ENAME 관리자의관리자명
FROM EMP E,EMP M1,EMP M2
WHERE E.MGR=M1.EMPNO(+) AND M1.MGR=M2.EMPNO(+);
--ANSI JOIN
SELECT E.ENAME 사원명, M1.ENAME 관리자명, M2.ENAME 관리자의관리자명
FROM EMP E LEFT OUTER JOIN EMP M1
ON E.MGR=M1.EMPNO 
LEFT OUTER JOIN EMP M2
ON M1.MGR=M2.EMPNO;